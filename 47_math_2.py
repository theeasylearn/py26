import math
x = 45
print("sinh",math.sinh(x))
print("cosh",math.cosh(x))
print("tanh",math.tanh(x))
x=math.radians(x)
print("radians of x",x)
print("sin(x)=",math.sin(x))
print("cos(x)",math.cos(x))
print("tan(x)",math.tan(x))

print("asin(x)=",math.asin(x))
print("acos(x)=",math.acos(x))
print("atan(x)=",math.atan(x))
print("degree(x)",math.degrees(x))
